public class Person
{
  private String name;
  private String color;
  private int age;

  
  public String getname()
  {
    return name;
  }

  
  public String getcolor()
  {
    return color;
  }

  
  public int getAge()
  {
    return age;
  }

  
  public void setAge(int ageVal)
  {
    age = ageVal;
  }

}